package day2;

public class CheckNumberIsPositive {

	public static void main(String[] args) {
		
		//Declare a with integer variable as version with value 10
		int version=10;
		
		if(version>0)
		{
			System.out.println("Version is Positive");
		}
		else if(version<0)
		{
			System.out.println("Version is Negative");
		}
		else
		{
			System.out.println("Version is Null / Zero");	
		}
		

	}

}
